package de.arthurpicht.utils.pdfbox2;

public class UtilsPdfbox2Exception extends Exception {

    public UtilsPdfbox2Exception() {
    }

    public UtilsPdfbox2Exception(String message) {
        super(message);
    }

    public UtilsPdfbox2Exception(String message, Throwable cause) {
        super(message, cause);
    }

    public UtilsPdfbox2Exception(Throwable cause) {
        super(cause);
    }

    public UtilsPdfbox2Exception(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
